#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	separator;

	if (argc < 1)
		return (0);
	separator = '\n';
	i = 0;
	while (argv[0][i] != '\0')
	{
		write(1, &argv[0][i], 1);
		i++;
	}
	write(1, &separator, 1);
}
